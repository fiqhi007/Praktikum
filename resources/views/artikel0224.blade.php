<h1>Artikel Framework</h1>
<p>Anda mungkin sedang bertanya: “Apa itu Framework?” Nah, framework adalah sebuah kerangka program yang digunakan untuk membantu developer untuk mengembangkan kode secara konsisten. 

Dengan adanya framework developer bisa mengurangi jumlah bug pada website atau aplikasi yang dibuat. Karena, fungsi dan variabel yang sudah tersedia di dalam komponen framework.

Dalam artikel ini, Anda akan mempelajari lebih dalam pengertian dan fungsi framework. Tak hanya itu, Anda pun akan diajak mengenal jenis jenis framework yang sering digunakan. Yuk, simak artikel ini sampai selesai.</p>
<h2>Apa Itu Framework?</h2>
<p>Framework adalah kerangka kerja yang digunakan untuk mengembangkan aplikasi berbasis desktop atau aplikasi berbasis website. Dengan menggunakan framework Anda akan lebih mudah untuk membuat aplikasi atau website. Itu karena Anda hanya perlu menyusun komponen-komponen pemrograman yang sudah jadi. Dengan kata lain, Anda tidak perlu membuat berbagai fitur dari awal lagi.<p>
<h2>Fungsi Framework</h2>
<p>Framework memiliki fungsi utama untuk mempermudah para developer mengembangkan aplikasi dan website terkait struktur MVC (Model View Controller) yang digunakan.<p>

